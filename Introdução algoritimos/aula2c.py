# Fatiamento
# Fatiamento de um pedaço de string 
s1 = 'Lógica de programção de algoritimos '
print (s1[0:6]) # fatiamento começa no numero 0 e termina aonde eu quero 6
s1 = 'Lógica de programção de algoritimos '
print (s1[24:34]) # fatiamento começa no numero 24 e termina aonde eu quero 34
s1 = 'Lógica de programção de algoritimos '
print (s1[24:]) # Aqui mostra aonde so o que eu quero

