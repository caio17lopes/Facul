#Função lambda
'''
funções masi simples, sem nome, chamadas de funções lambda
Elas pode mser escritas em uma so linha de codigo dentro do programa principal
'''
res = lambda x: x * x
print(res(3))

soma = lambda x,y: x + y
print(soma(3,5))

# Exercicio 
calc = lambda a,b: (a + 5)* b 
