'''
Introdução a padrões de design de software ( design pattern)
    São padrões de projeto
    São soluções reutilizaveis para problemas recorrentes no desenvolvimento de software 
    Representa abordagens testadas e comprovads para projetar e estruturar o código de forma eficiente
    Há varios tipos de design patterns, e cada um foca num aspecto do dev. de software

        Classificação em tres grandes gurpos 
            Padrões estruturais 
                Lidam com a organização de classes e objetos 
                Fornecem maneiras de compor estruturas maiores a partir de partes menores 
                    adpater , Proxy

            Padrões comportamentais 
                Lidam com a interação entre objetos e a definição de responsabilidade 
                Gereciam comunicação e comportamento entre diferentes objetos 
                    mediator,observer 

            Padrões de criação 
                Lidam com a criação de objetos 
                Oferecem mecanismos flexiveis para instanciação de classe
                    builder, factory 
'''
